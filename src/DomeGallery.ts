const DEFAULTS = {
  maxVerticalRotationDeg: 5,
  dragSensitivity: 20,
  enlargeTransitionMs: 300,
  segments: 35
};

const clamp = (v: number, min: number, max: number) => Math.min(Math.max(v, min), max);
const wrapAngleSigned = (deg: number) => {
  const a = (((deg + 180) % 360) + 360) % 360;
  return a - 180;
};


function buildItems(pool: any[], seg: number) {
  const xCols = Array.from({ length: seg }, (_, i) => -37 + i * 2);
  const evenYs = [-4, -2, 0, 2, 4];
  const oddYs = [-3, -1, 1, 3, 5];

  const coords = xCols.flatMap((x, c) => {
    const ys = c % 2 === 0 ? evenYs : oddYs;
    return ys.map(y => {
      const isCenter = c % 2 === 0 
        ? [-2, 0, 2].includes(y)
        : [-1, 1, 3].includes(y);
      return { x, y, sizeX: 2, sizeY: 2, isCenter, column: c };
    });
  });

  if (pool.length === 0) {
    return coords.map(c => ({ ...c, src: '', alt: '' }));
  }

  const normalizedImages = pool.map((image: any) => {
    if (typeof image === 'string') return { src: image, alt: '' };
    return { src: image.src || '', alt: image.alt || '' };
  });

  const uniqueImages = [...normalizedImages];
  // Shuffle to randomize placement
  for (let i = uniqueImages.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [uniqueImages[i], uniqueImages[j]] = [uniqueImages[j], uniqueImages[i]];
  }

  // Tracking image usage to satisfy column gap
  // imageSrc -> lastColumnUsed
  const lastUsedMap = new Map<string, number>();
  const middleRowAppearanceCount = new Map<string, number>();
  normalizedImages.forEach(img => {
    middleRowAppearanceCount.set(img.src, 0);
    lastUsedMap.set(img.src, -50); // Start far back
  });

  // Group slots by column for sequential processing
  const slotsByCol: Record<number, any[]> = {};
  coords.forEach(coord => {
    if (!slotsByCol[coord.column]) slotsByCol[coord.column] = [];
    slotsByCol[coord.column].push(coord);
  });

  const finalAssignments = new Map<any, any>();

  // Ensure all photos appear at least once in middle rows
  const unusedUnique = [...uniqueImages];
  
  for (let c = 0; c < seg; c++) {
    const colSlots = slotsByCol[c];
    
    // Process each slot in the column
    colSlots.forEach(slot => {
      // 1. Try to find an unused unique image that satisfies the gap
      // If the slot is center, we prioritize picking from unusedUnique
      let bestImg = null;
      
      if (slot.isCenter && unusedUnique.length > 0) {
        // Try to find one from unusedUnique that satisfies gap
        const candidateIdx = unusedUnique.findIndex(img => (c - lastUsedMap.get(img.src)!) >= 8);
        if (candidateIdx !== -1) {
            bestImg = unusedUnique.splice(candidateIdx, 1)[0];
        }
      }

      // 2. If center but no safe unused unique, or if periphery
      if (!bestImg) {
        const candidates = [...normalizedImages].sort(() => Math.random() - 0.5);
        bestImg = candidates.find(img => (c - lastUsedMap.get(img.src)!) >= 8);
      }

      // 3. Fallback: if STILL no bestImg (gap cannot be met), pick the least recently used one
      if (!bestImg) {
        bestImg = [...normalizedImages].sort((a, b) => lastUsedMap.get(a.src)! - lastUsedMap.get(b.src)!)[0];
      }

      finalAssignments.set(slot, bestImg);
      lastUsedMap.set(bestImg.src, c);
      if (slot.isCenter) {
        middleRowAppearanceCount.set(bestImg.src, (middleRowAppearanceCount.get(bestImg.src) || 0) + 1);
      }
    });
  }

  // Final emergency check: did we use all unique images in center rows?
  unusedUnique.forEach(img => {
      const centerSlotsWithReps = Array.from(finalAssignments.entries())
        .filter(([slot, assignedImg]) => slot.isCenter && middleRowAppearanceCount.get(assignedImg.src)! > 1);
      
      if (centerSlotsWithReps.length > 0) {
          const [slotToSwap, oldImg] = centerSlotsWithReps[0];
          finalAssignments.set(slotToSwap, img);
          middleRowAppearanceCount.set(oldImg.src, middleRowAppearanceCount.get(oldImg.src)! - 1);
          middleRowAppearanceCount.set(img.src, 1);
      }
  });

  return coords.map(coord => ({
    ...coord,
    ...finalAssignments.get(coord)
  }));
}


export async function initDomeGallery(
  containerSelector: string,
  options: any = {}
) {
  const {
    images = DEFAULT_IMAGES,
    fit = 0.5,
    fitBasis = 'auto',
    minRadius = 600,
    maxRadius = Infinity,
    padFactor = 0.25,
    overlayBlurColor = '#060010',
    maxVerticalRotationDeg = DEFAULTS.maxVerticalRotationDeg,
    dragSensitivity = DEFAULTS.dragSensitivity,
    enlargeTransitionMs = DEFAULTS.enlargeTransitionMs,
    segments = DEFAULTS.segments,
    imageBorderRadius = '30px',
    openedImageBorderRadius = '30px',
    grayscale = true
  } = options;

  const rootEl = document.querySelector(containerSelector) as HTMLElement;
  if (!rootEl) {
    console.error(`[DomeGallery] Container not found: ${containerSelector}`);
    return;
  }

  const rootRef = { current: rootEl };
  const mainRef = { current: null as HTMLElement | null };
  const sphereRef = { current: null as HTMLElement | null };
  const frameRef = { current: null as HTMLElement | null };
  const viewerRef = { current: null as HTMLElement | null };
  const scrimRef = { current: null as HTMLElement | null };
  const focusedElRef = { current: null as HTMLElement | null };

  const rotationRef = { current: { x: 0, y: 0 } };
  const startRotRef = { current: { x: 0, y: 0 } };
  const startPosRef = { current: null as any };
  const draggingRef = { current: false };
  const movedRef = { current: false };
  const openingRef = { current: false };
  const openStartedAtRef = { current: 0 };

  const scrollLockedRef = { current: false };
  const lockScroll = () => {
    if (scrollLockedRef.current) return;
    scrollLockedRef.current = true;
    document.body.classList.add('dg-scroll-lock');
  };
  const unlockScroll = () => {
    if (!scrollLockedRef.current) return;
    if (rootRef.current?.getAttribute('data-enlarging') === 'true') return;
    scrollLockedRef.current = false;
    document.body.classList.remove('dg-scroll-lock');
  };

  const items = buildItems(images, segments);

  // Preload images
  const preloadImages = (items: any[]) => {
    const promises = items.map(item => {
      if (!item.src) return Promise.resolve();
      return new Promise((resolve) => {
        const img = new Image();
        img.onload = resolve;
        img.onerror = resolve; // Continue even if some images fail
        img.src = item.src;
      });
    });
    return Promise.all(promises);
  };

  const imagesLoadedPromise = preloadImages(items);


  const applyTransform = (xDeg: number, yDeg: number) => {
    const el = sphereRef.current;
    if (el) {
      el.style.transform = `translateZ(calc(var(--radius) * -1)) rotateX(${xDeg}deg) rotateY(${yDeg}deg)`;
    }
  };

  const lockedRadiusRef = { current: null as number | null };

  // Build HTML structure
  rootEl.innerHTML = `
    <main class="sphere-main">
      <div class="stage">
        <div class="sphere"></div>
      </div>
      <div class="overlay"></div>
      <div class="overlay overlay--blur"></div>
      <div class="edge-fade edge-fade--top"></div>
      <div class="edge-fade edge-fade--bottom"></div>
      <div class="viewer">
        <div class="scrim"></div>
        <div class="frame"></div>
      </div>
    </main>
  `;

  mainRef.current = rootEl.querySelector('.sphere-main') as HTMLElement;
  sphereRef.current = rootEl.querySelector('.sphere') as HTMLElement;
  frameRef.current = rootEl.querySelector('.frame') as HTMLElement;
  viewerRef.current = rootEl.querySelector('.viewer') as HTMLElement;
  scrimRef.current = rootEl.querySelector('.scrim') as HTMLElement;

  // Set CSS variables
  rootEl.style.setProperty('--segments-x', segments.toString());
  rootEl.style.setProperty('--segments-y', segments.toString());

  // Populate sphere with items
  const sphere = sphereRef.current;
  items.forEach((item) => {
    const itemDiv = document.createElement('div');
    itemDiv.className = 'item';
    itemDiv.dataset.src = item.src;
    itemDiv.dataset.offsetX = item.x.toString();
    itemDiv.dataset.offsetY = item.y.toString();
    itemDiv.dataset.sizeX = item.sizeX.toString();
    itemDiv.dataset.sizeY = item.sizeY.toString();
    itemDiv.style.setProperty('--offset-x', item.x.toString());
    itemDiv.style.setProperty('--offset-y', item.y.toString());
    itemDiv.style.setProperty('--item-size-x', item.sizeX.toString());
    itemDiv.style.setProperty('--item-size-y', item.sizeY.toString());

    const imgDiv = document.createElement('div');
    imgDiv.className = 'item__image';
    imgDiv.setAttribute('role', 'button');
    imgDiv.setAttribute('tabindex', '0');
    imgDiv.setAttribute('aria-label', item.alt || 'Open image');

    const img = document.createElement('img');
    img.src = item.src;
    img.draggable = false;
    img.alt = item.alt;

    imgDiv.appendChild(img);
    itemDiv.appendChild(imgDiv);
    sphere.appendChild(itemDiv);
  });

  // Handle ResizeObserver
  const ro = new ResizeObserver(() => {
    const cr = rootEl.getBoundingClientRect();
    const w = Math.max(1, cr.width),
      h = Math.max(1, cr.height);
    const minDim = Math.min(w, h),
      maxDim = Math.max(w, h),
      aspect = w / h;
    let basis;
    switch (fitBasis) {
      case 'min':
        basis = minDim;
        break;
      case 'max':
        basis = maxDim;
        break;
      case 'width':
        basis = w;
        break;
      case 'height':
        basis = h;
        break;
      default:
        basis = aspect >= 1.3 ? w : minDim;
    }
    let radius = basis * fit;
    const heightGuard = h * 1.35;
    radius = Math.min(radius, heightGuard);
    radius = clamp(radius, minRadius, maxRadius);
    lockedRadiusRef.current = Math.round(radius);

    const viewerPad = Math.max(8, Math.round(minDim * padFactor));
    rootEl.style.setProperty('--radius', `${lockedRadiusRef.current}px`);
    rootEl.style.setProperty('--viewer-pad', `${viewerPad}px`);
    rootEl.style.setProperty('--overlay-blur-color', overlayBlurColor);
    rootEl.style.setProperty('--tile-radius', imageBorderRadius);
    rootEl.style.setProperty('--enlarge-radius', openedImageBorderRadius);
    rootEl.style.setProperty('--image-filter', grayscale ? 'grayscale(1)' : 'none');
    applyTransform(rotationRef.current.x, rotationRef.current.y);
  });
  ro.observe(rootEl);

  // Drag handling
  mainRef.current?.addEventListener('pointerdown', (e: PointerEvent) => {
    if (focusedElRef.current) return;
    draggingRef.current = true;
    movedRef.current = false;
    startRotRef.current = { ...rotationRef.current };
    startPosRef.current = { x: e.clientX, y: e.clientY };
  });

  const handlePointerMove = (e: PointerEvent) => {
    if (focusedElRef.current || !draggingRef.current || !startPosRef.current) return;
    movedRef.current = true;

    const dx = e.clientX - startPosRef.current.x;
    const dy = e.clientY - startPosRef.current.y;

    const nextX = clamp(
      startRotRef.current.x - dy / dragSensitivity,
      -maxVerticalRotationDeg,
      maxVerticalRotationDeg
    );
    const nextY = wrapAngleSigned(startRotRef.current.y + dx / dragSensitivity);
    rotationRef.current = { x: nextX, y: nextY };
    applyTransform(nextX, nextY);
  };

  document.addEventListener('pointermove', handlePointerMove);

  document.addEventListener('pointerup', () => {
    draggingRef.current = false;
    movedRef.current = false;
  });

  // Close enlarged image
  const closeEnlarge = () => {
    if (performance.now() - openStartedAtRef.current < 250) return;
    const el = focusedElRef.current;
    if (!el) return;
    const parent = el.parentElement as HTMLElement;
    const overlay = viewerRef.current?.querySelector('.enlarge') as HTMLElement;
    if (!overlay) return;

    overlay.remove();
    parent.style.setProperty('--rot-y-delta', '0deg');
    parent.style.setProperty('--rot-x-delta', '0deg');
    el.style.visibility = '';
    el.style.zIndex = '0';
    focusedElRef.current = null;
    rootEl?.removeAttribute('data-enlarging');
    openingRef.current = false;
    unlockScroll();
  };

  scrimRef.current?.addEventListener('click', closeEnlarge);
  window.addEventListener('keydown', (e: KeyboardEvent) => {
    if (e.key === 'Escape') closeEnlarge();
  });

  // Open image handler
  const openItemFromElement = (imgEl: HTMLElement) => {
    if (openingRef.current || movedRef.current || draggingRef.current) return;
    openingRef.current = true;
    openStartedAtRef.current = performance.now();
    lockScroll();

    const parent = imgEl.parentElement as HTMLElement;
    focusedElRef.current = imgEl;

    imgEl.setAttribute('data-focused', 'true');
    imgEl.style.visibility = 'hidden';
    imgEl.style.zIndex = '0';

    const overlay = document.createElement('div');
    overlay.className = 'enlarge';
    overlay.style.position = 'absolute';
    overlay.style.zIndex = '30';

    const rawSrc = parent.dataset.src || imgEl.querySelector('img')?.src || '';
    const img = document.createElement('img');
    img.src = rawSrc;
    overlay.appendChild(img);
    viewerRef.current?.appendChild(overlay);

    overlay.style.opacity = '0';
    overlay.style.transition = `opacity ${enlargeTransitionMs}ms ease`;

    setTimeout(() => {
      overlay.style.opacity = '1';
      rootEl?.setAttribute('data-enlarging', 'true');
    }, 16);
  };

  // Add click handlers to all tiles
  const tiles = rootEl.querySelectorAll('.item__image');
  tiles.forEach(tile => {
    (tile as HTMLElement).addEventListener('click', () => {
      if (!movedRef.current && !draggingRef.current && !openingRef.current) {
        openItemFromElement(tile as HTMLElement);
      }
    });
  });

  applyTransform(0, 0);

  return {
    ready: imagesLoadedPromise,
    spin: (durationMs: number = 2000, revolutions: number = 1) => {
      const startY = rotationRef.current.y;
      const startTime = performance.now();

      const animateSpin = (now: number) => {
        const elapsed = now - startTime;
        const progress = Math.min(elapsed / durationMs, 1);
        
        // Use an easing function for smooth start/stop
        const easeInOut = (t: number) => t < 0.5 ? 2 * t * t : -1 + (4 - 2 * t) * t;
        const currentY = startY + (360 * revolutions * easeInOut(progress));
        
        rotationRef.current.y = wrapAngleSigned(currentY);
        applyTransform(rotationRef.current.x, rotationRef.current.y);

        if (progress < 1) {
          requestAnimationFrame(animateSpin);
        }
      };

      requestAnimationFrame(animateSpin);
    },
    destroy: () => {
      ro.disconnect();
      document.removeEventListener('pointermove', handlePointerMove);
      document.body.classList.remove('dg-scroll-lock');
    }
  };
}
